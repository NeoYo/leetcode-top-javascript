// @lc code=start
/**
 * @param {string} s
 * @return {number}
 */
var lengthOfLongestSubstring = function(s) {
    let max = 0;
    let map = new Map();
    for (let i = 0, j = 0; j < s.length; j++) {
        const char = s[j];
        i = Math.max(map.get(char) || 0, i);
        map.set(char, j + 1);
        max = Math.max(max, j - i + 1);        
    }
    return max;
};
